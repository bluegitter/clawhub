const messages: Record<Locale, Record<MessageKey, string>> = { en, zh };

type TranslateParams = Record<string, string | number>;

type LocaleContextValue = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: MessageKey, params?: TranslateParams) => string;
  formatDate: (value: string | number | Date) => string;
  formatDateTime: (value: string | number | Date) => string;
  formatCompactNumber: (value: number) => string;
};

function translate(locale: Locale, key: MessageKey, params?: TranslateParams) {
  const template = messages[locale][key] ?? messages.en[key];
  if (!params) return template;
  return Object.entries(params).reduce<string>(
    (result, [paramKey, value]) => result.replaceAll(`{${paramKey}}`, String(value)),
    template,
  );
}

export function resolveLocale(value?: string | null): Locale {
  return value === "zh" ? "zh" : "en";
}

export function getPreferredLocale(): Locale {
  if (typeof window === "undefined") return DEFAULT_LOCALE;

  const url = new URL(window.location.href);
  const queryLocale = resolveLocale(url.searchParams.get("lang"));
  if (queryLocale !== DEFAULT_LOCALE || url.searchParams.get("lang") === "en") {
    return queryLocale;
  }

  const stored = resolveLocale(window.localStorage.getItem(LOCALE_STORAGE_KEY));
  if (stored !== DEFAULT_LOCALE || window.localStorage.getItem(LOCALE_STORAGE_KEY) === "en") {
    return stored;
  }

  const browserLocale = window.navigator.languages
    .map((entry) => entry.toLowerCase())
    .find((entry) => entry.startsWith("zh"));

  return browserLocale ? "zh" : DEFAULT_LOCALE;
}

function formatCompactNumberForLocale(locale: Locale, value: number) {
  if (!Number.isFinite(value)) return "0";
  return new Intl.NumberFormat(locale, {
    notation: "compact",
    compactDisplay: "short",
    maximumFractionDigits: 1,
  }).format(value);
}

function formatDateForLocale(locale: Locale, value: string | number | Date) {
  return new Intl.DateTimeFormat(locale, {
    year: "numeric",
    month: "short",
    day: "numeric",
  }).format(new Date(value));
}

function formatDateTimeForLocale(locale: Locale, value: string | number | Date) {
  return new Intl.DateTimeFormat(locale, {
    year: "numeric",
    month: "short",
    day: "numeric",
    hour: "numeric",
    minute: "2-digit",
  }).format(new Date(value));
}

const LocaleContext = createContext<LocaleContextValue>({
  locale: DEFAULT_LOCALE,
  setLocale: () => {},
  t: (key, params) => translate(DEFAULT_LOCALE, key, params),
  formatDate: (value) => formatDateForLocale(DEFAULT_LOCALE, value),
  formatDateTime: (value) => formatDateTimeForLocale(DEFAULT_LOCALE, value),
  formatCompactNumber: (value) => formatCompactNumberForLocale(DEFAULT_LOCALE, value),
});

export function LocaleProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocale] = useState<Locale>(() => getPreferredLocale());

  useEffect(() => {
    window.localStorage.setItem(LOCALE_STORAGE_KEY, locale);
    document.documentElement.lang = locale;
  }, [locale]);

  return (
    <LocaleContext.Provider
      value={{
        locale,
        setLocale,
        t: (key, params) => translate(locale, key, params),
        formatDate: (value) => formatDateForLocale(locale, value),
        formatDateTime: (value) => formatDateTimeForLocale(locale, value),
        formatCompactNumber: (value) => formatCompactNumberForLocale(locale, value),
      }}
    >
      {children}
    </LocaleContext.Provider>
  );
}

export function useI18n() {
  return useContext(LocaleContext);
}
